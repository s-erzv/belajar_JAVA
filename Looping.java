public class Looping {
    public static void main(String[] args) {
       
        // For
        for(int i=2; i<9; i++){
            System.out.println(i);
        }
        
        // While
        int j = 5;
        while (j<10){
            System.out.println(j);
            j++;
        }

        // Do While
        int k=6;
        do{
            System.out.println(k);
            k++;
        }while (k<7);
    }
}
